enum class ColorCode
{
	Black,
	Red,
	Green,
	Yellow,
	Blue,
	Magenta,
	Cyan,
	White
};

namespace
{

template<typename T>
ostream& operator<<(ostream& os, const vector<T>& row)
{
	for (auto f : row) {
		string prefix = "";
		string postfix = "";
		if (f == Correct) {
			prefix = BoldGreenPrefix;
			postfix = ResetColor;
		}
		os << prefix << f << postfix;
	}
	return os;
}

template<typename T, ColorCode color>
string boldColorPrint(const T& text)
{
	string out = "";
	switch (color) {
	case ColorCode::Black: break;
	case ColorCode::Red: {
		out += BoldRedPrefix;
		break;
	}
	case ColorCode::Green: {
		out += BoldGreenPrefix;
		break;
	}
	case ColorCode::Yellow: break;
	case ColorCode::Blue: break;
	case ColorCode::Magenta: break;
	case ColorCode::Cyan: break;
	case ColorCode::White: break;
	default: break;
	}
	out += text + ResetColor;
	return out;
}

template <typename T>
using ColorText = string(*)(const T&);
ColorText<string> BoldRed = boldColorPrint<string, ColorCode::Red>;
ColorText<string> BoldGreen = boldColorPrint<string, ColorCode::Green>;

} //unnamed namespace

Game::Game() : maxAttempts(10), codeLength(4)
{
}

Game::~Game()
{
}

vector<int> Game::readGuess() {
	char c;
	vector<int> guess;
	while (cin >> c) {
		guess.push_back(atoi(&c));
		if (guess.size() == (unsigned) (codeLength)) break;
	}

	return guess;
}

void Game::play()
{
	Board board(codeLength);
	cout << "Game started, you have " << maxAttempts << " chances!" << endl;
	for (int attempt = 1; attempt <= maxAttempts; ++attempt) {
		cout << "Attempt #" << attempt << ": ";
		vector<char> hint = board.getHint(readGuess());
		cout << hint << endl;
		if (board.isCodeCracked(hint)) {
			cout << BoldGreen("Code is correct, you won!!!") << endl;
			return;
		}
	}
	cout << "You lost :(." << "The code is: " << BoldRed(board.secretCode()) << endl;
}

